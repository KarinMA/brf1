        <a name="meddelanden" style="margin-left:5px;">
            <h5>Meddelanden:</h5></a>
        <p> Här ser du dina meddelanden du fått från medlemmarna i föreningarna du ansvarar för. En röd ikon visas när du har fått ett eller flera nya meddelanden. Klicka på ”läs mer” för att öppna hela meddelandet efter att du har klickat på inkorgen. Du kan svara på meddelandet genom att klicka på svara.  Skriv sedan meddelandet du vill skicka i den stora rutan och tryck på skicka knappen när du är klar. </p>
        <p>&nbsp;</p>
        <a name="aktiveringslank">
            <h5>Aktiveringslänk:</h5></a>
        <p>Här skapar du den aktiveringslänk som du sedan kan kopiera och maila till en styrelsemedlem. Om föreningen aktiverar sin hemsida genom att klicka på länken du skickat kommer du att som mäklare automatiskt att bli kopplad till denna förening.</p>
        <p>Skriv in namnet på föreningen du vill skicka en aktiveringslänk till. Välj förening i listan som visas.</p>
        <p>När du valt förening skapas en länk som du kopierar och lägger in i det mail du skickar till den styrelsemedlem du har kontakt med.</p>
        <p>OBS för att vi skall veta att det är just du som mäklare som medverkat till att aktivera en förening är det viktigt att du meddelar föreningen att använda aktiveringslänken du skickat. Sidan är gratis och föreningen kan därför aktivera sidan själva. Skulle detta ske är det viktigt att du mailar oss snarast så att vi kan koppla på dig som Föreningens Mäklare. Skulle två mäklare kontakta samma förening tillfaller platsen som Föreningens Mäklare den som skickat länken som föreningen använt vid registreringen. Vid annat fall den mäklare som mailat oss på Svensk Brf först. </p>
