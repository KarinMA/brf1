            <h5>Din profil:</h5></a>
        <p>Här ser du din Profil i egenskap av Svensk Brf anknuten fastighetsmäklare. Denna visas i samtliga föreningar som du står som Föreningens Mäklare. Du kan när som helst ändra din profil genom att gå in i inställningar i menyn till vänster. </p>
        <p>På denna sida syns även vilka föreningar du är kopplad till. Klicka på respektive förening för att komma in i profilen som visas på respektive sida. Du kan här skriva ett meddelande som endast medlemmarna i just den föreningen kan ta del av.</p>
