        <a name="installningar" style="margin-left:5px;">
            <h5>Inställningar:</h5></a>
        <p>Här kan du lägga in en ny bild eller byta bild. Du kan även lägga in eller redigera  den text som visas  i samtliga föreningar som du är kopplad i. Klicka i bildrutan för att ladda upp en bild och fyll i fälten med de uppgifter du vill visa för föreningen. Du måste lägga in din mail adress om du vill kunna ta emot meddelanden från sidan till din jobbmail. Här kan du även byta lösenord och namn. Här väljer du även vilket företag du arbetar för. Du kan också välja att visa för medlemmarna i föreningarna vilka övriga föreningar du är kopplad till. Detta för att öka trovärdigheten, ju fler föreningar desto större uppfattas lokalkännedomen och sannolikheten att du, som fastighetsmäklare, har ett gediget spekulant register. 
        </p>
