        <a name="styrelselogg" style="margin-left:5px;">
            <h5>Styrelselogg:</h5></a>
        <p>Styrelseloggen är ett redskap som styrelsen har till sitt förfogande där de kan spara vesäntliga händelser som skett I föreningen. Här kan man även ladda upp dokument som tex offerter, fakturor och annat som är av betydelse för föreningen. Det kan vara skönt att ha allt samlat på samma ställe så att nya styrelsemedlemmar enkelt och snabbt kan sätta sig in I tidigare och kommande projekt samt följa de diskutioner som förts.</p>
        <img style="margin-left:15px; border:1px solid #CCC;" src="<?php echo BASE_DIR; ?>media/inloggad/img/faq/logg.png" width="400" height="224" alt="logg" />
        <p>&nbsp;</p>
        <a name="gor_inlagg" style="margin-left:5px;">
            <h5>Gör inlägg:</h5></a>
        <p>Klicka på gör inlägg under styrelseloggen. Ni kommer då till en ny sida där det finn en sk “drop down meny” Där väljer ni sedan under vilken kategori som ni vill göra inlägget. Vill ni skapa ett nytt projekt så väljer ni &quot;Nytt projekt&quot; i listan. Nu skapas en ny textruta där ni skriver det namnet på projektet. Och en till ruta där ni beskriver projektet. </p>
        <p>Sedan gör ni ett inlägg genom att först fylla I den rubriken du vill att inlägget skall ha och sedan göra du själva inlägget genom att skriva I rutan inlägg. När du är klar klickar du på spara. Då  blir inlägget synligt för samtliga styrelsemedlemmar. </p>
        <img style="margin-left:15px; border:1px solid #CCC;" src="<?php echo BASE_DIR; ?>media/inloggad/img/faq/inlagg_styrelsen.png" width="395" height="453" alt="Gör inlägg" />
        <p>&nbsp;</p>
        <a name="kommentar" style="margin-left:5px;">
            <h5>Kommentar till inlägg:</h5></a>
        <p>Du kan kommentera ett inlägg som gjorts under styrelseloggen. Du skriver därefter din kommentar I rutan under inlägget. När du är klar klickar då på det “lägg till kommentar” då sparas din komentar under inlägget i styrelseloggen. Framför varje kommentar kan du se vem som har gjort kommentaren och när. <br/><br/>
            Här kan ni ladda upp dokument till specifika projekt. Välj projekt i listan nedan. Därefter trycker ni på knappen Välj fil och väljer det dokument ni vill ladda upp. Vill ni skapa ett nytt projekt för det aktuella dokumentet  så väljer ni Nytt projekt. Ni kan sedan skapa en rubrik för dokumentet tex offert fasadrenovering och även beskriva projektet I rutan beskrivning. När ni är klara så klickar ni på spara ikonen. </p>
        <img style="margin-left:15px; border:1px solid #CCC;" src="<?php echo BASE_DIR; ?>media/inloggad/img/faq/kommentarer.png" width="396" height="384" alt="kommentarer" />
        <p>&nbsp;</p>
        <a name="maklarinformation" style="margin-left:5px;">
            <h5>Mäklarinformation:</h5></a>
        <p>När en lägenhet ska säljas brukar en mäklare höra av sig till ordförande i föreningen. Genom att fylla i nedanstående uppgifter har du alltid dokumentet klart och slipper fylla i vid varje försäljning. Ju noggrannare du fyller I formuläret desto mindre frågor kan du förvänta dig av fastighetsmäklare och spekulanter på lägenheter som är till salu I föreningen. </p>
        <p>Vi rekomenderar att ni efter varje styrelsemöte uppdaterar informationen I formuläret om något har eller kommer att ändras. Frågorna och svaren ni fyller I här blir synligt på föreningens offentliga sida. Dels under mäklarinformation dels uppdelat I olika kategorier,  för att ge bra översikt.</p>
