        <a name="sms">
            <h5>SMS:</h5></a>
        <p>Här gör du inställningar för SMS, samt skickar sms. Om din förening inte önskar använda sms-funktionen bockar du ur de förbockade rutorna nedan. Tryck på spara för att genomföra ändringen. Varje sms kostar 1.20 kr. Svensk Brf stödjer det livräddande arbete som Läkare Utan Gränser gör i över 70 länder världen över. Därför har vi valt att låta 5% av kostnaden för varje sms gå till deras verksamhet.</p>
        <img style="margin-left:15px; border:1px solid #CCC;" src="<?php echo BASE_DIR; ?>media/inloggad/img/faq/sms1.png" width="352" height="268" />
        <p>För att skicka sms skriver du ditt meddelande i rutan nedan. Vill du skicka meddelandet till alla medlammar i föreningen bockar du för rutan framför &quot;Skicka till alla&quot;. Vill du skicka till en eller flera utvalda meddlemmar klickar du på länken &quot;Välj mottagare&quot;. När du gjort dina val klickar du på &quot;skicka&quot;</p>
