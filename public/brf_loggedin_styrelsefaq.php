            <h5>Kalender:</h5></a>
        <p>Här skapar du nya kalenderdagar. Välj vilken dag och månad det gäller genom att klicka på ett datum i kalendern (skärmdump med pilar). Skriv in rubrik, välj tiden på dagen och gör en kort beskrivning om vad som kommer att hända i fältet &quot;Om kalendardagen&quot; Du kan även välja att koppla på sms function på kalendedagen I fråga genom att klicka I rutan som heter sms påminnelse (skärmdump med pil). </p>
